package shamim.lecture5;

public class IfElse {
    public static void main(String[] args) {
        int x = 20;
        if(x<20){
            System.out.println("x is less than 20.");
        }else if(x>20){
            System.out.println("x is greater than 20.");
        }else{
            System.out.println("x is equal to 20.");
        }
    }
}
