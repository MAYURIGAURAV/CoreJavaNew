package patternExample;

public class Pattern_4 {
    public static void main(String[] args) {
        char i;
        char j;
        for (i = 'A'; i <= 'E'; i++) {
            for (j = 'A'; j <= 'E'; j++) {
                System.out.print(i);
            }
            System.out.println();
        }
    }
}
