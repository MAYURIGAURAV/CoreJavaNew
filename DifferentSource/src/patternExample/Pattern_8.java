package patternExample;

public class Pattern_8 {
    public static void main(String[] args) {
        char i;
        char j;
        for (i = 'E'; i >= 'A'; i--) {
            for (j = 'E'; j >= 'A'; j--) {
                System.out.print(i);
            }
            System.out.println();
        }
    }
}
