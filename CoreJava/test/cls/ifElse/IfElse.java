package cls.ifElse;

public class IfElse {
    public static int x = 5;
    public static int y = 10;
    
    public static void main(String[] args) {
        if(x>y){
            System.out.println("The large value: "+x);
        }else{
            System.out.println("The large value: "+y);
        }
    }
}
